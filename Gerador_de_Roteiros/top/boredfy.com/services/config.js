// Função para gerar vozes Chirp3 HD para um idioma
function generateChirp3Voices(languageCode) {
  const voices = [];
  CHIRP3_HD_VOICES.forEach((voice) => {
    voices.push(`${languageCode}-Chirp3-HD-${voice}`);
  });
  return voices;
}

// Função para separar vozes por gênero (baseado no padrão da documentação)
function separateVoicesByGender(voices) {
  const feminine = [];
  const masculine = [];

  voices.forEach((voice) => {
    // Baseado na documentação, algumas vozes são femininas e outras masculinas
    // Vou usar um padrão simples baseado no nome da voz
    if (
      [
        "Achernar",
        "Algenib",
        "Alnilam",
        "Autonoe",
        "Callirrhoe",
        "Despina",
        "Erinome",
        "Gacrux",
        "Kore",
        "Laomedeia",
        "Leda",
        "Pulcherrima",
        "Sadaltager",
        "Sulafat",
        "Vindemiatrix",
        "Zephyr",
      ].includes(voice.split("-").pop())
    ) {
      feminine.push(voice);
    } else {
      masculine.push(voice);
    }
  });

  return { feminine, masculine };
}

